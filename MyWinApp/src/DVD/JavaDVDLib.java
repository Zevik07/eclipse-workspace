    private void cboCodeItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboCodeItemStateChanged
        // TODO add your handling code here:
        try {
            Statement cmd = con.createStatement();
            ResultSet rs = cmd.executeQuery("SELECT * FROM DVD WHERE DVDCodeNo=" + cboCode.getSelectedItem());
            if (rs.next()) {
                txtCodeNo.setText(rs.getInt(1) + "");
                txtTitle.setText(rs.getString(2));
                cboLang.setSelectedItem(rs.getString(3));
                if (rs.getString(4).equals("true")) {
                    radYes.setSelected(true);
                } else {
                    radNo.setSelected(true);
                }
                updPrice.setValue(rs.getInt(5));
            }
            //
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Lỗi truy vấn");
        }
    }//GEN-LAST:event_cboCodeItemStateChanged

