    private void btnSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSaveMouseClicked
        // TODO add your handling code here
        try {
            //Check state of delete_fag
            if (delete_flag == 1) {

                Statement cmd = con.createStatement();
                int rs = cmd.executeUpdate("DELETE FROM DVD WHERE DVDCodeNo=" + cboCode.getSelectedItem());
                JOptionPane.showMessageDialog(this, "Đã xóa thành công!");
                cboCode.removeItem(cboCode.getSelectedItem());
                delete_flag = 0;
            } 
            else {
                //Check txtCodeNo in db, insert or update
                Statement check = con.createStatement();
                ResultSet checkrs = check.executeQuery("Select * from DVD where DVDCodeNo=" + txtCodeNo.getText());
                if (checkrs.next()) {
//                      checkrs.updateString("DVDTitle",txtTitle.getText().toString());
//                      checkrs.updateString("DVDLang",cboLang.getSelectedItem().toString());
//                      checkrs.updateString("DVDSubTitles",((Boolean) radYes.isSelected()).toString());
//                      checkrs.updateInt("DVDPrice",(int)updPrice.getValue());
                      
                    String query = "Update DVD set DVDTitle='" + txtTitle.getText()
                            + "', DVDLang='" + cboLang.getSelectedItem() + "', DVDSubTitles='"
                            + ((Boolean) radYes.isSelected()).toString() + "', DVDPrice=" + updPrice.getValue()
                            + " where DVDCodeNo=" + txtCodeNo.getText();
                    int checkreturn = check.executeUpdate(query);
                    JOptionPane.showMessageDialog(this, "Đã cập nhật thành công!");
                } else {
                    Statement cmd = con.createStatement();
                    String sql = "INSERT INTO DVD VALUES ("
                            + txtCodeNo.getText() + ",'"
                            + txtTitle.getText() + "','"
                            + cboLang.getSelectedItem() + "','"
                            + ((Boolean) radYes.isSelected()).toString() + "',"
                            + updPrice.getValue()
                            + ")";
                    int rs = cmd.executeUpdate(sql);
                    JOptionPane.showMessageDialog(this, "Đã thêm thành công!");

                }
            }
            //Thay doi trang thai cac nut nhu cu
            reset_BtnState();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Lỗi truy vấn");
        }
    }//GEN-LAST:event_btnSaveMouseClicked
    private void reset_BtnState() {
        //Thay doi trang thai cac nut nhu cu
        btnAddNew.setEnabled(true);
        btnDelete.setEnabled(true);
        btnUpdate.setEnabled(true);
        btnSave.setEnabled(false);
        btnCancel.setEnabled(false);
    }
    private void btnAddNewMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddNewMouseClicked
        // TODO add your handling code here:
        try {
            //Show DVDCodeNo textbox
            txtCodeNo.setVisible(true);
            cboCode.setVisible(false);
            //Reset button state
            reset_BtnState();
            //Query database to ++txtCodeNo
            Statement cmd = con.createStatement();
            ResultSet rs = cmd.executeQuery("SELECT MAX(DVDCodeNo) FROM DVD");
            if (rs.next()) {
                txtCodeNo.setText(rs.getInt(1) + 1 + "");
            } else {
                txtCodeNo.setText("0");
            }
            btnSave.setEnabled(true);

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Lỗi truy vấn");
        }
    }//GEN-LAST:event_btnAddNewMouseClicked

